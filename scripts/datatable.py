def create_json():
    data = {'data': []}

    for key, value in input.iteritems():
        names = key.split('.')
        if len(names) == 1:
            category = None
            name = names[0]
        elif len(names) == 2:
            category = names[0]
            name = names[1]
        elif len(names) == 3:
            category = names[0]
            name = '.'.join([names[1], names[2]])

        data['data'].append(OrderedDict([
            ('category', category),
            ('name', name.replace('_', ' ')),
            ('agency', value),
            ('contact', choice(contact)),
            ('restrictions', choice(restrictions)),
            ('governance', choice(governance)),
            ('links', '<i class="fa fa-mixcloud fa-fw"></i><i class="fa fa-cube fa-fw"></i><i class="fa fa-cubes fa-fw"></i><i class="fa fa-file-archive-o fa-fw"></i>')
        ]))

    data['data'].sort(key = lambda x: x['category'])

    return data

